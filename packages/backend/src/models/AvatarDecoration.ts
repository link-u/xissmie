import { Entity, PrimaryColumn, Index, Column, ManyToOne, JoinColumn } from 'typeorm';
import { id } from './util/id.js';

@Entity('avatar_decoration')
export class MiAvatarDecoration {
	@PrimaryColumn(id())
	public id: string;

	@Column('timestamp with time zone', {
		nullable: true,
	})
	public updatedAt: Date | null;

	@Column('varchar', {
		length: 1024,
	})
	public url: string;

	@Column('varchar', {
		length: 256,
	})
	public name: string;

	@Column('varchar', {
		length: 2048,
	})
	public description: string;

	// ストアの商品かどうか
	@Column('boolean', {
		default: false,
	})
	public isInStore: boolean;

	@Column('varchar', {
		length: 128, nullable: true,
	})
	public storeProductId: string | null;

	@Column('varchar', {
		length: 128, nullable: true,
	})
	public storeAuthorId: string | null;

	@Column('varchar', {
		length: 256, nullable: true,
	})
	public storeAuthorName: string | null;

	// TODO: 定期ジョブで存在しなくなったロールIDを除去するようにする
	@Column('varchar', {
		array: true, length: 128, default: '{}',
	})
	public roleIdsThatCanBeUsedThisDecoration: string[];
}
